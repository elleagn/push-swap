#include "push_swap.h"

int	main(int ac, char **av)
{
	int	i;

	i = 1;
	if (ac > 1)
	{
		while (av[i])
		{
			ft_putstr_fd(av[i], 1);
			ft_putchar_fd('\n', 1);
			i++;
		}
	}
	return (0);
}
